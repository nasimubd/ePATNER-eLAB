<script>
    function openRejectModal(paymentId, businessName) {
        document.getElementById('rejectModal').classList.remove('hidden');
        document.getElementById('rejectForm').action = `/super-admin/subscriptions/payments/${paymentId}/reject`;
        document.querySelector('#rejectModal h3').textContent = `Reject Payment - ${businessName}`;
    }

    function closeRejectModal() {
        document.getElementById('rejectModal').classList.add('hidden');
        document.getElementById('reason').value = '';
    }
</script>
